def flat_p3_items() -> list[dict]:
    items = []
    for group in P3_GROUPS:
        for code, title, purpose in group["items"]:
            items.append(
                {
                    "code": code,
                    "group_code": group["code"],
                    "group_title": group["title"],
                    "title": title,
                    "purpose": purpose,
                }
            )
    return items


